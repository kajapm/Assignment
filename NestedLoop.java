public class NestedLoop 
{
    public static void main(String ags[]) //right angle triangle pattern
    {
        for(int i=0; i<=10; i++)
        {
            for(int j=0;j<=i;j++)  //example for nested loop
            {
                System.out.print(" * ");
            }
            System.out.println("");
        }
    }
}
